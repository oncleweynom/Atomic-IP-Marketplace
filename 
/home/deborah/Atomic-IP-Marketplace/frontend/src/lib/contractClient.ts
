/**
 * Fetch ALL listings by scanning sequential IDs.
 * For demo/low listings. Limit to avoid RPC abuse.
 */
export async function getAllListings(maxScan = 1000): Promise<Listing[]> {
  console.log(`Fetching listings 1-${maxScan} from contract...`);
  const promises = Array.from({ length: maxScan }, (_, id) => getListing(id + 1));
  const listings = await Promise.all(promises);
  const validListings = listings.filter((l): l is Listing => l !== null);
  console.log(`Found ${validListings.length} listings`);
  return validListings;
}

