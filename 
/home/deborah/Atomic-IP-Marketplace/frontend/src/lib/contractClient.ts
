function toHex(v: Uint8Array | ArrayBuffer | null) {
  if (!v) return '';
  const bytes = v instanceof Uint8Array ? v : new Uint8Array(v);
  return Array.from(bytes)
    .map(b => b.toString(16).padStart(2, '0'))
    .join('');
}

