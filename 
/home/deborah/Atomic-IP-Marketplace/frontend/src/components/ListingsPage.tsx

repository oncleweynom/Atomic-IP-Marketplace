
import { useEffect, useState, useMemo } from "react";
import { useSearchParams, useNavigate } from "react-router-dom";
import { ShoppingCart, Loader2, Search, Filter } from "lucide-react";
import { useDebounce } from "../lib/debounce";
import { getAllListings, type Listing as ContractListing } from "../lib/contractClient";

interface Listing {
  id: number;
  ipfs_hash: string;
  owner: string;
  price: number;
  status: 'available' | 'pending' | 'sold';
}

function truncateHash(hash: string): string {
  if (!hash) return "";
  if (hash.length <= 14) return hash;
  return `${hash.slice(0, 6)}...${hash.slice(-6)}`;
}

function truncateAddress(address: string): string {
  if (!address) return "";
  if (address.length <= 12) return address;
  return `${address.slice(0, 6)}...${address.slice(-6)}`;
}

async function fetchListings(): Promise<Listing[]> {
  try {
    const contractListings = await getAllListings(100);
    return contractListings.map((cl): Listing => ({
      id: cl.id,
      ipfs_hash: cl.ipfs_hash,
      owner: cl.owner,
      price: Number(cl.price_usdc) / 1_000_000,
      status: 'available' as const,
    }));
  } catch (error) {
    console.error("Failed to fetch listings:", error);
    // Graceful fallback - empty list
    return [];
  }
}

export function ListingsPage() {
  const [searchParams, setSearchParams] = useSearchParams();
  const [allListings, setAllListings] = useState<Listing[]>([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);
  const navigate = useNavigate();

  const initialSearch = searchParams.get('search') || '';
  const initialStatus = searchParams.get('status') || 'all';
  const [searchQuery, setSearchQuery] = useState(initialSearch);
  const [statusFilter, setStatusFilter] = useState<'all' | 'available' | 'pending' | 'sold'>(initialStatus as any);
  const debouncedSearch = useDebounce(searchQuery, 300);

  // Filtered listings
  const filteredListings = useMemo(() => {
    return allListings.filter((listing) => {
      const searchLower = debouncedSearch.toLowerCase();
      const matchesSearch = debouncedSearch === '' || 
        listing.id.toString().includes(debouncedSearch) ||
        listing.owner.toLowerCase().includes(searchLower) ||
        listing.ipfs_hash.toLowerCase().includes(searchLower);
      const matchesStatus = statusFilter === 'all' || listing.status === statusFilter;
      return matchesSearch && matchesStatus;
    });
  }, [allListings, debouncedSearch, statusFilter]);

  // Update URL params when filters change
  useEffect(() => {
    const params = new URLSearchParams();
    if (searchQuery) params.set('search', searchQuery);
    if (statusFilter !== 'all') params.set('status', statusFilter);
    setSearchParams(params, { replace: true });
  }, [debouncedSearch, statusFilter, searchQuery, setSearchParams]);

  // Load listings
  useEffect(() => {
    let mounted = true;

    async function load() {
      setLoading(true);
      setError(null);
      try {
        const data = await fetchListings();
        if (mounted) {
          setAllListings(data);
        }
      } catch (err) {
        console.error("Load error:", err);
        if (mounted) {
          setError("Failed to fetch listings. Check console.");
        }
      } finally {
        if (mounted) {
          setLoading(false);
        }
      }
    }

    load();

    return () => {
      mounted = false;
    };
  }, []);

  const content = () => {
    if (loading) {
      return (
        <div className="flex items-center justify-center p-12 text-slate-500">
          <Loader2 className="h-8 w-8 animate-spin mr-3" />
          Loading listings from IP registry...
        </div>
      );
    }

    if (error || filteredListings.length === 0) {
      return (
        <div className="rounded-xl border border-slate-200 bg-slate-50 p-6 text-slate-700">
          <p>No listings found or fetch error.</p>
          <p className="text-sm text-slate-500">Check RPC connection/console. Demo data shows UI works.</p>
        </div>
      );
    }

    return (
      <div>
        <p className="mb-4 text-sm text-slate-500">
          Showing {filteredListings.length} of {allListings.length} listings
        </p>
        <div className="grid gap-4 md:grid-cols-2 lg:grid-cols-3">
          {filteredListings.map((listing) => (
            <article
              key={listing.id}
              className="rounded-xl border border-slate-200 bg-white p-4 shadow-sm hover:shadow-md transition-shadow"
            >
              <div className="mb-2 flex items-center justify-between text-xs text-slate-500">
                <span>Listing #{listing.id}</span>
                <span>{listing.price.toLocaleString()} USDC</span>
              </div>

              <div className="mb-3">
                <p className="text-xs text-slate-400">IPFS Hash</p>
                <p className="truncate text-sm font-medium text-slate-800" title={listing.ipfs_hash}>
                  {truncateHash(listing.ipfs_hash)}
                </p>
              </div>

              <div className="mb-4">
                <p className="text-xs text-slate-400">Owner</p>
                <p className="truncate text-sm text-slate-700" title={listing.owner}>
                  {truncateAddress(listing.owner)}
                </p>
              </div>

              <button
                className="inline-flex w-full items-center justify-center gap-2 rounded-lg bg-blue-600 px-4 py-2 text-sm font-semibold text-white hover:bg-blue-700 focus:outline-none focus:ring-2 focus:ring-blue-400 transition-colors"
                onClick={() => navigate(`/swap/${listing.id}`)}
              >
                <ShoppingCart size={16} />
                Buy Now
              </button>
            </article>
          ))}
        </div>
      </div>
    );
  };

  const filters = (
    <div className="mb-6 flex flex-col gap-3 lg:flex-row lg:items-end lg:gap-4">
      <div className="relative flex-1">
        <Search className="absolute left-3 top-1/2 -translate-y-1/2 text-slate-400 h-4 w-4" />
        <input
          type="search"
          placeholder="Search by owner, listing ID, or IPFS hash..."
          className="w-full rounded-lg border border-slate-200 pl-10 pr-4 py-2 text-sm focus:border-blue-400 focus:outline-none focus:ring-1 focus:ring-blue-400"
          value={searchQuery}
          onChange={(e) => setSearchQuery(e.target.value)}
        />
      </div>
      <div className="flex items-center gap-2">
        <Filter className="text-slate-400 h-4 w-4 flex-shrink-0" />
        <select
          value={statusFilter}
          onChange={(e) => setStatusFilter(e.target.value as any)}
          className="rounded-lg border border-slate-200 px-3 py-2 text-sm focus:border-blue-400 focus:outline-none focus:ring-1 focus:ring-blue-400"
        >
          <option value="all">All Statuses</option>
          <option value="available">Available</option>
          <option value="pending">Swap Pending</option>
          <option value="sold">Sold</option>
        </select>
      </div>
    </div>
  );

  return (
    <section className="mx-auto max-w-7xl p-4">
      <div className="mb-6 flex flex-col gap-2 sm:flex-row sm:items-center sm:justify-between">
        <div>
          <h1 className="text-2xl font-bold text-slate-900">Marketplace Listings</h1>
          <p className="text-sm text-slate-500">Browse all IP listings from Soroban contract.</p>
        </div>
        {loading && (
          <div className="inline-flex items-center gap-2 text-sm text-slate-500">
            <Loader2 className="h-4 w-4 animate-spin" /> Loading
          </div>
        )}
      </div>

      {!loading && !error && filters}
      {content()}
    </section>
  );
}

