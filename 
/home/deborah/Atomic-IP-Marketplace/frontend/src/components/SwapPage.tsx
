
import { useParams, useNavigate } from "react-router-dom";
import { useEffect, useState } from "react";
import { Loader2, Copy } from "lucide-react";
import { getListing } from "../lib/contractClient";

export function SwapPage() {
  const { id } = useParams<{ id: string }>();
  const navigate = useNavigate();
  const [listing, setListing] = useState<any>(null);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);

  useEffect(() => {
    async function loadListing() {
      if (!id) return;
      try {
        setLoading(true);
        setError(null);
        const data = await getListing(Number(id));
        setListing(data);
      } catch (err) {
        console.error(err);
        setError(`Listing ${id} not found.`);
      } finally {
        setLoading(false);
      }
    }
    loadListing();
  }, [id]);

  if (loading) {
    return (
      <section className="mx-auto max-w-3xl p-6">
        <div className="flex items-center justify-center gap-2 text-slate-500 py-12">
          <Loader2 className="h-6 w-6 animate-spin" />
          Loading listing details...
        </div>
      </section>
    );
  }

  if (error || !listing) {
    return (
      <section className="mx-auto max-w-3xl p-6">
        <h1 className="mb-4 text-2xl font-bold text-slate-900">Listing Not Found</h1>
        <p className="mb-6 text-slate-600">{error}</p>
        <button
          className="rounded-lg bg-slate-800 px-4 py-2 text-white hover:bg-slate-900"
          onClick={() => navigate(-1)}
        >
          Back to Listings
        </button>
      </section>
    );
  }

  const price = Number(listing.price_usdc) / 1_000_000;

  return (
    <section className="mx-auto max-w-3xl p-6">
      <button
        className="mb-6 rounded-lg bg-slate-200 px-4 py-2 text-slate-800 hover:bg-slate-300"
        onClick={() => navigate(-1)}
      >
        ← Back to Listings
      </button>
      
      <header className="mb-6">
        <h1 className="text-2xl font-bold text-slate-900">Buy Listing #{listing.id}</h1>
        <p className="text-slate-600">Initiate atomic swap for this IP listing.</p>
      </header>

      <div className="bg-slate-50 p-6 rounded-xl mb-8">
        <h2 className="text-lg font-semibold mb-4">Listing Details</h2>
        <div className="grid gap-4 mb-6">
          <div>
            <label className="text-xs text-slate-500 mb-1 block">IPFS Hash</label>
            <div className="flex items-center gap-2">
              <code className="font-mono text-sm break-all flex-1 bg-slate-100 px-3 py-1 rounded">
                {listing.ipfs_hash}
              </code>
              <button className="p-1 hover:bg-slate-200 rounded" title="Copy">
                <Copy size={14} />
              </button>
            </div>
          </div>
          <div>
            <label className="text-xs text-slate-500 mb-1 block">Owner</label>
            <code className="font-mono text-sm bg-slate-100 px-3 py-1 rounded inline-block">
              {listing.owner.slice(0, 28)}...
            </code>
          </div>
          <div>
            <label className="text-xs text-slate-500 mb-1 block">Merkle Root</label>
            <code className="font-mono text-xs opacity-75">
              {listing.merkle_root.slice(0, 16)}...
            </code>
          </div>
        </div>
        <div className="border-t pt-4">
          <div className="text-2xl font-bold text-slate-900">
            {price.toLocaleString()} USDC
          </div>
          <div className="text-xs text-slate-500">Royalty: {listing.royalty_bps / 100}% to {listing.royalty_recipient.slice(0, 28)}...</div>
        </div>
      </div>

      <div className="text-center p-12 border-2 border-dashed border-slate-200 rounded-xl bg-slate-50">
        <h3 className="font-semibold text-slate-900 mb-2">Ready to swap</h3>
        <p className="text-slate-600 mb-4">Connect wallet → Approve USDC → Initiate atomic swap</p>
        <p className="text-xs text-slate-500">✅ Contract integration complete, wallet context needed for buy flow.</p>
      </div>
    </section>
  );
}

